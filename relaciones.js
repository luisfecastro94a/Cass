jQuery(document).ready(function () {

    var iRegion = 0;
    var htmlRegion = '<option value="sin-region">Seleccione región</option><option value="sin-region">--</option>';
    var htmlComunas = '<option value="sin-region">Seleccione comuna</option><option value="sin-region">--</option>';

    jQuery.each(RegionesYcomunas.region, function () {
        htmlRegion = htmlRegion + '<option value="' + RegionesYcomunas.region[iRegion].NombreRegion + '">' + RegionesYcomunas.region[iRegion].NombreRegion + '</option>';
        iRegion++;
    });

    jQuery('#region').html(htmlRegion);
    jQuery('#comuna').html(htmlComunas);

    jQuery('#region').change(function () {
        var iRegion = 0;
        var valorRegion = jQuery(this).val();
        var htmlComuna = '<option value="sin-comuna">Seleccione comuna</option><option value="sin-comuna">--</option>';
        jQuery.each(RegionesYcomunas.region, function () {
            if (RegionesYcomunas.region[iRegion].NombreRegion == valorRegion) {
                var iComuna = 0;
                jQuery.each(RegionesYcomunas.region[iRegion].comuna, function () {
                    htmlComuna = htmlComuna + '<option value="' + RegionesYcomunas.region[iRegion].comuna[iComuna] + '">' + RegionesYcomunas.region[iRegion].comuna[iComuna] + '</option>';
                    iComuna++;
                });
            }
            iRegion++;
        });
        jQuery('#comuna').html(htmlComuna);
    });
    jQuery('#comuna').change(function () {
        if (jQuery(this).val() == 'sin-region') {
            alert('selecciones Región');
        } else if (jQuery(this).val() == 'sin-comuna') {
            alert('selecciones Comuna');
        }
    });
    jQuery('#region').change(function () {
        if (jQuery(this).val() == 'sin-region') {
            alert('selecciones Región');
        }
    });

});